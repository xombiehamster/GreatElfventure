<p>“Not Lycans, not monsters, and not magic. We found hoofprints, but otherwise just normal footprints. All of them were killed by normal weapons. It was, most likely, either bandits or another group of refugees.” Dhakamari spread the maps and notes out on the table. “As far as we can tell, nothing but animals have been there since the attack, and the attack happened at least five years ago. It’s quiet, but not too quiet, if you know what I mean.”</p>

<p>“So it’s safe?” Baijani asked.</p>

<p>“Unless something new comes into the area, yes.” He smiled, “And we’ll keep up patrols to make sure we know if that happens.”</p>

<p>Baijani looked over at Shadimon, her gold eyes sharp and intense against her dark skin. He nodded once, and she breathed out and inclined her head before turning to leave.</p>

<p>At Dhakamari’s quizzical look, Shadimon twitched his feathers to settle them, “She wants to take care of the dead.”</p>

<p>“Ahh...We’ll keep watch.”</p>

<hr/>

<p>“We’ve named the village,” Anasatri declared, coming up to where Shadimon was perched on the clifftop.</p>

<p>Shadimon blinked, “I...see. And what are we calling it?”</p>

<p>“Asavardi. No one’s sure who started it, but after a few days everyone was using it.” She grinned, “So I’m telling you so it becomes official.”</p>

<p>He smiled, “I like it. Asavardi it is.”</p>

<hr/>

<p>While Shadimon spent his days out on patrol with the rest of the Harriers, the rest of the elves threw themselves into building with renewed vigor. They were still nervous, but as the days passed uneventfully while the Harriers and rangers stayed on patrol, some of the tension eased. Spaces further away from the canyons had been set aside for farms, but during the evening meals, Shadimon overheard excited talk about gardens.</p>

<p>“Oh yes,” Anasatri said when he asked. “Several people got to talking, and they want to make the walls more green. There are already ledges with plants growing on them, and they want to expand those and start actually planting things wherever they can. Tassimir suggested placing greenery to look like a canopy and sculpting the cliff into tree trunks. The walkways can be made to look like branches, and we can have our homes in the ‘trees’ like we used to! We can carry the plants all the way down to the canyon floor so there’s proper undergrowth. And of course most of these plants would be edible as well as decorative.”</p>

<p>Shadimon felt his eyebrows going higher and higher, and worked to keep his ears from flicking down. “That’s...extremely ambitious.”</p>

<p>“Of course! As things settle and we start improving the houses, well, caves, but eventually houses, we can start on really going into that. But for now, even just trying to see if it’s possible to get things to grow there is good to know.”</p>

<p>“I’m planning to ask the dark elves for advice and maybe the loan of some builders when we go back to the fort this summer. Hopefully they’ll have had time to settle too and can spare us a few people.”</p>

<p>“That’s a good idea. We’ve got the artistic part down, but stone doesn’t act the way we’re used to, and we can’t really be sure of what areas are safe and stable. We’ll run out of big enough caves eventually. Baijani has been busy out at that burned village, by the way.”</p>

<p>“She’d told me she was going, but I don’t know her actual plans.”</p>

<p>Anasatri took a drink; with the river, water was one thing they had no concerns about. “She rounded up a handful of volunteers, and they’re going to bury the bodies. That seems to be what a lot of ground-bound do.” Her wings shivered, “I wouldn’t like it, myself. But, from what I’ve heard, a lot of them find <i>our</i> way uncomfortable.”</p>

<p>“And not practical, at this point. So they’re going to bury them.”</p>

<p>“Individually. Baijani said we’ve had too much of mass graves.”</p>

<p>“She’s right. What else?”</p>

<p>“They’re not going to try to save the buildings. They’re mostly burned up, and it’s too much of a reminder. She has a few people plotting ideas for a formal memorial, but I don’t know the details.” She played with one of her bracelets, “It’s going to take a long time though. There aren’t many volunteers.”</p>

<p>“That may change as time goes on. Things are quiet. The rangers haven’t found anything interesting, and we haven’t found anything to fight, and word of that has gone around. People know what Baijani is up to?”</p>

<p>“Sure. I’m surprised you hadn’t heard more detail already.”</p>

<p>“I’ve been a little busy,” Shadimon said dryly. “We don’t gossip much on patrol.”</p>

<p>“No gossip! That must get boring!”</p>

<p>Shadimon smiled, but it was slight.</p>

<p>Anasatri grimaced and put her hand on his wrist, “Ah, not for joking, sorry Shadi.”</p>

