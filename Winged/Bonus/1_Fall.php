<p>Many of the bodies had been scavenged by local animals, so they mostly had to guess on which bones went with which. They did the best they could, laying the charred bones on blankets or tarps to be carried to where graves were being dug. Everyone, even the elves who couldn’t bring themselves to help with the reclamation, offered feathers or jewelry, or even simple pendants to mark the graves. </p>

<p>As each building was cleared, other teams started the process of dismantling them. Most of them were rough-cut wood, and all were badly damaged. The charred wood was taken to be turned into ash compost for the communal farms, and what little remained was used for grave markers. </p>

