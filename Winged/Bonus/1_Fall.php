<h1>Fall year 1: Turn 2</h1>

<p>Shadimon shrugged his armor on, shifting his wings to settle the straps running down his back. The mottled blue and grey leather bore many carefully mended scars, and one of the panels was clearly newer than the rest. It was almost meditative, going through the motions again.</p> 

<p>Their armor was minimal; it had to be, for them to be able to fly with it. A light leather breastplate and pauldrons protected their torso, but only from the front. Shadimon grimaced, <i>If anyone is behind us, we already have bigger problems.</i> Bracers and greaves protected the limbs. Pouches strapped to waist and thigh held a variety of possible weapons; Shadimon favored a net and bolas, in addition to the standard javelins. Either was extremely effective against another flier, and even the enormous Lycan bats they’d fought against were susceptible to a long fall and a hard landing. </p>

<p>Winged elf fighters had been rare before the War. They as a culture tended toward diplomacy and peace, but hunting at high speeds through dense forest had given them speed and agility that the larger Lycans just couldn’t match. Harriers were the product of the War; nothing like them had existed before, but, if Shadimon had his way, they wouldn’t fade with time. </p>

<p>He paused, running a strap through his hands. He had started the War as just a ranger in their home forest. Though he didn’t remember a time before the Lycans, he did remember when the Empire’s restrictions started strangling the region. All it had taken was one young elf raising a fuss in the wrong place at the wrong time, and one Lycan guard deciding that lethal force was the proper response to adolescent bluster. The escalation had been inevitable. </p>

<p><i>We still didn’t know then just what they were willing to do.</i></p>

<p>It had never even occurred to any of the winged elves that the Lycans would take a full scorched-earth approach. The Ulanya forest had been growing for tens of thousands of years, and there was nothing like it anywhere in the world. The Lycans burned it down, with the winged elves in it, and razed the ground it stood on. </p>

<p>Leather creaked in Shadimon’s hand. He had been one of the rangers who saw them coming, who saw the smoke. As horrific as it was, it hadn’t been an utter slaughter. Many of the winged elves had been retreating, out of the forest and further into the wilds, linking up with other discontented groups. But, while many of the people had escaped, too many had died, and the loss of the forest was a devastating blow. </p>

<p>Shadimon tightened the last straps and shouldered a light quiver of javelins. He dearly hoped none of this was needed except as a reassurance, but he had not brought his people to this point only for them to lose a home again.</p>

