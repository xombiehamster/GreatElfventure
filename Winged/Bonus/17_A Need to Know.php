<h1>Need to Know</h1>

<p>At the end of the season, Anasatri came back to Asavardi. Something had told her to wait before returning, but she was worried that she had waited too long and might have missed others leaving for Surt. As she flew in, she could see a lot of activity below that seemed...odd for this time of day. She flew directly to Town Hall, landing and looking around. She found Baijani near the back, looking tired. "Jani? What happened?"</p>

<p>Baijani looked up, "Oh, Satri, welcome home. How was the observatory?"</p>

<p>"Fine, and I have to talk to you about something, but...what's going on here?"</p>

<p>Baijani sighed, letting her wings droop, "We were attacked by the same coyote elves that hit the dreamdust clans. We drove them off, thank Her name, but we just got word from Dust that...Shadi was shot down and captured. By them."</p>

<p>Anasatri sat down with a thump on a nearby stool. "Oh goddess, is he alive?"</p>

<p>"For now. Dust has been sending him dream messages, and they're going through, so he's still alive for now at least. But Alenwe went with him to scout out the enemy town - Shadi told him to run - and he said it's a real city, with walls and a large civilian population. It's not the shantytown or bandit camp we were hoping for." She rubbed her eyes, "We're getting ready to leave for Surt in a couple days. I'm reluctant to leave, but, I need to be there."</p>

<p>"So do I."</p>

<p>Baijani looked up with a faint frown, "Do you."</p>

<p>Anasatri drummed her fingers on the table, her feathers rippling in an agitated wave. "I do, yes. And now you'll ask me why. I...had a vision. Something is going to happen in Surt, on that night. I don't know what, but it's important."</p>

<p>Baijani sat up straighter, "What can you tell me?"</p>

<p>Anasatri looked around, "Can...we go somewhere else? Or can you shield us? It's...it's that important."</p>

<p>"So I see. Come, we'll go to my home. If anyone else is there, they'll leave for awhile."</p>

<p>Two of Baijani's granddaughters were home, but cheerfully cleared out when asked. Baijani settled in the sitting area with Anasatri across from her. "So, let's hear it."</p>

<p>"First, the vision felt like it was...in confidence. I don't feel like I can share most of it. I haven't even told anyone else I had it. But you understand." She folded her hands, "I need to start from the beginning, I think. You've heard the reports from the observatory, and how weirdly esoteric the organization is. We're getting solid chunks of it, but there's some overall pattern that we just can't see. The Loremaster had died, so we had no one there to ask. Except..."</p>

<p>Baijani's eyebrows went up, "Did you..."</p>

<p>"Luna. I did. And...I got a response. There's something...something wrong. I don't understand the vision, yet, but there's something going to happen at Surt, and...somewhere else. Another place that I want to find after the meeting. Need to find."</p>

<p>Baijani studied her for a long moment, "Shrike has insisted that we take some scouts with us for safety, and I'm taking Dhakamari as well for tactical information if it's needed. Take at least one of them with you after. Where are you going to go?"</p>

<p>"It's along the river, close to Alfyr. Within a few days flight if we have trouble. But I have to. It's-" She paused, grimacing, "It's important. Something is wrong. I can't...I can't say more in good conscience..."</p>

<p>"No, I understand." She smiled crookedly, "Just remember to say hello to Calestros on occasion too. It's only polite."</p>

