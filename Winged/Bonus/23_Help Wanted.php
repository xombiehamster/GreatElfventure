<h1>Help Wanted 2</h1>

<p>The path to Shadimon's house had been smoothed and reinforced in places, but the biggest change was the large tiered garden beds. Shadimon paused, bemused, to examine the planters that were shedding flowers onto his doorstep. He had known Tassimir's plans, of course, but there had been so many frustrations and setbacks, he hadn't really been able to imagine the sheer scope of those plans. Now, looking out over the canyon, the riot of greenery draping the walls did perfectly accent the carved trees. The perpetual wind brought the smell of soil and growing things, and his wings shivered as his shoulders lost some of their tension.</p>

<p>The porch outside his house had...grown. He paused again, blinking at the polished wood that had very much not been there before. It was a smooth half-circle, offering plenty of landing space. There was even a bench near the edge, looking outward, and some more planters full of flowers. The edges had been decorated with colorful paper streamers and rope. He glanced at the house with some trepidation, but the shutters were closed, and he couldn't see inside.</p>

<p>The door opened as he was contemplating, and Tathariel stepped out, distracted. When he looked up and noticed Shadimon, he obviously had to restrain himself from running over. "Shadimon! I wasn't sure if you'd be here today, but I didn't want to leave anything outside..."</p>

<p>"Outside?"</p>

<p>"Yes, ah, here, let me get out of your way." He pushed the door open wider and stepped back.</p>

<p>Shadimon peeked through the door somewhat hesitantly, but nothing appeared majorly different. However, when he looked to the side of the door, the counter leading into the kitchen was piled with...something. Nearly a dozen bundles of all sizes had been stacked on the counter. "Um...what?"</p>

<p>Tathariel tucked his wings in, "Ah...People started leaving things when we heard you were back. I haven't opened any of them, but I've heard people talking so I'd guess it's useful things? Blankets, clothes, house things, that sort of thing."</p>

<p>Shadimon felt an entirely unexpected burn in his eyes. Several of the bundles had feathers attached to them. "I...just didn't expect..." He blinked rapidly and cleared his throat, "I hope you haven't been taking a lot of time over here while I was away."</p>

<p>Tathatiel's ears flicked down and he looked slightly embarrassed. "Ah, well...Not like you're thinking."</p>

<p>Shadimon's eyebrows went up as he investigated the first of the packages.</p>

<p>"Finished space is still kind of hard to come by, so I don't really have a workshop. It's not a priority, so I just haven't...you know, asked for it. So I mostly do my work at home. I have a big family and a lot of them are in and out so it gets pretty loud sometimes. So...I sometimes brought things here to work on..." He rubbed his hands together, "I'm sorry, that's not why I'm supposed to be here."</p>

<p>Shadimon chuckled, "No point in the place sitting empty. Are you also behind my new front porch?"</p>

<p>"No, actually. I couldn't get in for a week while that was being done." He held up his hands, "Honest. The building crew decided on their own to do it."</p>

<p>The first package did indeed have a blanket, very soft and with a simple, colorful pattern. The second had one of the long bands of cloth they used in place of shirts. It was clearly modeled on the Harrier armor, with a soft gradient in blue-greys and subtle cloud patterning. Each of the packages was something small, something simple, ranging from clothing to kitchen supplies, but each one had been given out of care, and concern.</p>

<p>Tathariel gently put his hand on Shadimon's shoulder, "We missed you," he said quietly. "And we were afraid for you."</p>

<p>"I'm just one person," Shadimon said, his voice rough.</p>

<p>"One person who&rsquo;s important to the rest of us." Tathariel paused, looking at the pile of gifts, "Ah, now that you're officially back, some of the gifts will probably be food."</p>

<p>"Wh...there...more?" Shadimon stumbled.</p>

<p>Tathariel was very carefully not looking at Shadimon's wings, but there was no hiding how much weight he had lost. "Like I said, people worry. We don't know what happened," he looked apologetic, "but we know it wasn't good. People want to help."</p>

<p>Shadimon looked down at the new cup in his hands, fingers tracing the divide between slick paint and rougher clay, "Thank you." He set the cup on one of the rough shelves, and started tidying the stack of gifts. "I'm not sure how much I'm up for company, but I would appreciate if you continued coming in to check on things. And you're free to work here, too. Conversation might be a bit much for now, but quiet company could be...nice. Actually..."</p>

<p>He glanced up at Tathariel, "You're an enchanter, right?"</p>

<p>"Mhm. There hasn't been a lot of call for it lately, but I had a few ideas..."</p>

<p>Shadimon nodded, "I'd like to send...some kind of gifts, to the people who saved me. Something more useful than sentimental, but I have no real ideas beyond that. So I might come to you for thoughts on that."</p>

<p>Tathariel grinned, "Happily! I'd love to have a project or three." His smile softened, "I'll leave you to settle in for now though. And I'll let people know to...go easy for a little while."</p>

<p>"Thank you. Come by anytime."</p>

<hr>

<p>Tathariel tapped on the open door and peeked inside, "Shadimon?"</p>

<p>"In the bedroom, be out in a minute!"</p>

<p>Tathariel had his bag of supplies with him, and sat down in the nest. The low table was littered with papers and a waxed tablet, and he carefully stacked those out of the way before laying out his things. His familiar, Drop-It, was poking around among the cushions, looking for any stray crumbs. "Kalavani said you already picked up the statues from her?"</p>

<p>There was a thump from the bedroom, "Not on the table? Uh, by the door?"</p>

<p>Tathariel looked around and found a small bundle on a stool near the door. "Ah, yes I found it." He glanced toward the open bedroom door, "Do you need help in there?"</p>

<p>"No thanks. I';m just putting up some shelves. It's just awkward, but not difficult." After some more thumping, Shadimon came out and dropped into the nest. He gathered up the papers Tathariel had moved, flipping through and rearranging them.</p>

<p>"Sorry, did I mess something up?"</p>

<p>"No, no. They weren't really in any order."</p>

<p>"What are you working on?"</p>

<p>Shadimon sighed gustily, "Not so much working as trying to catch up. A lot happened while I was gone. Jani kept track of it, of course, but I'm trying to fill in details. The attack, sure, but also this situation with Vailenren..."</p>

<p>Tathariel pursed his lips, turning the small copper goat over in his hands, "I could tell you about that. It was a community decision, since everyone was affected by it."</p>

<p>"That's what Jani said. She didn't feel like it was her call to make."</p>

<p>"And most people think she was right. No one was happy about it, but at least most people agreed with the solution. Hey, Drop-it, can you get me that smaller bag please?"</p>

<p>The raven quarked at him and pulled a cloth pouch out of his larger bag. Mission accomplished, she sidled over toward Shadimon, keeping one eye on Tathariel. She clicked her beak at Shadimon, fluffing up her neck feathers, "Pet please?"</p>

<p>"You're shameless," Tathariel muttered, flipping through his spellbook.</p>

<p>Drop-it clicked her beak again and made a cheerful buzzing sound as Shadimon scratched her neck. She understood Elven perfectly well, but her speech was hard to understand sometimes. She mostly stuck with simple phrases, though Shadimon had heard her and Tathariel have intense one-sided conversations. Some kind of magic, Tathariel said, letting familiars of any kind speak with their bonded mage in some kind of common language. Drop-it was just unusual in that she was a raven and could actually speak.</p>

<p>She immediately lost interest in Shadimon, petting or no, when Tathariel tipped out a bit of glittering red dust. "Shiny?" She hopped across the table, "Shiny? Shiny shiny!" Tathariel automatically held up a hand to keep her back while his other hand traced the dust into the proper shape. "Yes I know, keep your feathers on." He set the small copper goat in the middle of the sigil, murmuring quietly as he did. The dust started to glow, finally igniting on the last word. Tathariel traced his fingers through the glittering fire, drawing a new shape that pulled the glow into the copper. When the haze of magic cleared, the small copper statue was glowing strongly.</p>

<p>Drop-it pecked at it a time or two before picking it up and parading around the table as Tathariel picked up another statue, this one a delicate violin. Tathariel peered at it, admiring the clearly defined strings and tuning pegs. "How in the world does she do this?"</p>

<p>"Says the other artist," Shadimon snorted. "I've seen your work, and I have no idea how you do the tooling and beading that you do without going blind."</p>

<p>Tathariel's wings dipped and he laughed a little, "Right, fair. Metal just seems less forgiving."</p>

