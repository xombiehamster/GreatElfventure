<p>“Home” is just a word. To the dreamdust elves, it’s always been a bit of a fluid term. Home is where your friends and family are, not a physical place. They have always been used to a nomadic life, but war was different. Even though they did not suffer as much as some of their allies, the sheer horrors that they were exposed to - and force to commit - kept them from ever feeling like they belonged. Now, though, they have used their skills to forge peace instead of war. For the first time in a long time, the dreamdust elves are starting to feel like they have a place they belong.
</p>

<p>You are refugees no more.
</p>

