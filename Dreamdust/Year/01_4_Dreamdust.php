<h2>Turn 4 Results
</h2>
<p><b>Economic-</b> While the initial trading with the Machakw is a little tense, after a few weeks of interaction things start to warm up considerably. The Machakw do actually have quite a bit worth trading, especially information. They have quite a wealth on info on the local terrain, food stuffs, and water sources. Their crafts don’t have quite the detailed quality of a more technologically advanced culture, but they make equipment and particularly shelters that are sturdy, lightweight, and easily concealed. They also turn out to be great at finding things, like particular herbs, animals, or raw materials. They begin to work with your traders to get what they need, and by the end of the season your trading hub is starting to finally fulfill its intended use.
</p>

<p>The Machakw are indeed becoming your friends, though it’s still a somewhat tenuous association.
</p>

<p><b>Political-</b> While you are more than happy to make friends and invite even a former enemy into your camp, the dreamdust elves aren’t stupid. Betrayal is always a possibility. Your spyhunters keep a close eye on the visiting Machakw, using their thought sense to get a good reading on their intentions.
</p>

<p>Thankfully, they appear earnest. They don’t trust you yet, but they don’t seem to be intending to hurt you or go back on their deal. Mostly, they are curious and more than a little hungry - at least until they trade for more food. You appear to have made a good impact with that.
</p>

<p><b>Military-</b> Your Pathfinders make a little slower progress than usual, honestly probably because of those fancy cacti discovered last turn. Despite this, they are able to scout the two squares of the Haven river adjacent to Riverhaven. Great naming there.
</p>

<p>At any rate, they both contain the Cold River and Fertile Desert aspects. There’s nothing exceptionally unique discovered in either square, but the Haven river has exceptionally cool and pristinely clean water due to whatever its source is. It may be possible to take advantage of this, but at the bare minimum it makes the plant life around the river verdant.
</p>
<p>Interestingly, Tik’Tak negotiates an agreement with a few of his scout friends among the Machakw. As part of their trade with you, they agree to lend their help as Pathfinders for a season. Using them will give you a boost die on pathfinders.
</p>

<p>They also reveal the current location of Tk’eek to you, 3 squares east and 1 square north of Riverhaven. It is now marked on your map.
</p>

<p><b>Mental-</b> Good googly moogly, Machakw is a hard language to learn. It’s so far away from normal lizardfolk it might as well be a different language entirely. There’s a lot of head scratching and frustration. Still, your efforts are far from wasted. The Flappy manages to get a hang of it, as well as one of your leaders (your choice.) Most importantly though, the cooperation with the Machakw goes great. All the Machakw you meet are able to learn Common, and Lee’Shaa and Tik’Tak both are able to learn how to read. Lee’Shaa promises to bring it back to her people and spread it as much as she can. Indeed, Lee’Shaa appears to be working on a written version of her language.
</p>

<p>In return for this great gift you have given them, the Machakw have an offer for you if you so choose to take it. They offer to teach you some of the secrets of this desert that have kept them alive so far. For exact stats, see below.
</p>

<p>Ironically, all this examination of language and speech creates a solid core of linguistics experts in your settlement. Any further checks to learn about languages or create aspects relating to language will have the difficulty reduced by one.
</p>

<p><b>Event:</b> Raiders
</p>
<p><b>Status:</b> Resolved
</p>

<p>The dreamdust elves have put the brakes on violence and created peace. In a situation where most others would have resorted to armed conflict, the dreamdust elves were able to find a solution that killed no one. Indeed, the dreamdust elves did not kill a single Machakw, even after they had lost one of their own. The significance of this is not lost on the Machakw.
</p>

<p>While the Machakw and dreamdust elves are still learning how to get along, the first few months have gone well. “Trust” is a strong word, but the Machakw have agreed to share several of their secrets that they hold quite close. Also, importantly, they’re not trying to take your stuff anymore. The event is resolved and any stress damage will begin to heal naturally next turn.
</p>

<p>Due to the to the incredible patience, wisdom, and carefully applied violence it took for the dreamdust elves to make their new peace with the Machakw, they receive a permanent upgrade to one of the following skills: Trade, Special Operations, or Mysticism
</p>

