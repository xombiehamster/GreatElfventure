<h2><strong>Constabulary Guard</strong></h2>
<p>For as much as they are frequently referred to as little more than decorative pickpocket deterrents, there is a core of hardened soldiers at the CG's heart. Spearheaded by Doukas, these elves were almost all experienced blade elf soldiers before the Conflagration and make up the majority of the CG's officers and NCOs. They fought through the height of the war, and have the scars to prove it - both mentally and physically.</p>
<p>Most of them are people who decided to leave war behind, but still craved the structure of military life. The CG was a good fit for them. They have no interest in deploying or going on campaign, but they are fully prepared to lay down their lives in defense of Surt.</p>
<p>From these former soldiers, Doukas is easily able to create an emergency muster unit that is specifically trained to defend the city in the event of a hostile military attack. This new "Division" is attached to Surt. It cannot be moved from Surt and still remain a functional unit. If the soldiers that make up that Division leave the city, it will cease to function as a unit.</p>
<p>There is some amount of controversy here. Most of these CG soldiers were loyalists. Unlike the Charred Eagles, most of these soldiers were part of the blade elf garrison at Muspelham. They did not join the rebels until forced to, when Dreyrugr was at the gates. There is an old wound here, but Doukas manages to get it under control. He makes a surprisingly passionate speech, declaring that the entire purpose of this is to make sure that they never make the same mistake again. It's enough.</p>
